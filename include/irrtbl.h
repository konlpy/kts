/* 0 : '으' 탈락 : trigger character의 바로 왼쪽에 '_'를 삽입           
					 (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */
/* 1 : 'ㄹ' 탈락 : trigger character의 바로 오른쪽에 'L'을 삽입 : 
			   오른쪽에는 { N | n | B | si | o | L | s9 } 중 하나
			   왼쪽에는 초성이 오면 안됨 : 즉 종성 혹은 중성만 온다       */
/* 2 : 'ㅅ' 불규칙 : trigger character의 바로 왼쪽에 'S' 삽입             */ 
/* 3 : 'ㄷ' 불규칙 : trigger character의 바로 왼쪽 character를 'D'로 바꿈 */
/* 4 : 'ㅂ' 불규칙 : trigger character를 'B'으로 바꿈                     */
/* 5 : '르' 불규칙 : trigger character의 바로 왼쪽 character를 '_'로 바꿈 */
/* 6 : '러' 불규칙 : trigger character의 바로 왼쪽 character를 제거       */
/* 7 : '우' 불규칙 : trigger character의 바로 왼쪽에 'u' 삽입             */
/* 8 : '여' 불규칙 : trigger character의 바로 왼쪽 character를 제거       */
/* 9 : '거라'불규칙: trigger character의 두번째 왼쪽 character를 제거     */
/* 10: '너라'불규칙: trigger character의 세번째 왼쪽 character를 제거     */
/* 11: 'ㅎ' 불규칙 : trigger character의 바로 왼쪽에 'H' 삽입             */
/* 12: 'ㅎ' 불규칙 : trigger character를 'aHa'/ 'eHe'중 하나로 
					 앞의 것이 양성이냐 음성이냐에 따라서 구분            */
/* 13: '애,에'축약 : trigger character뒤에 'e'를 삽입 , 앞/뒤를 모두 본다 
					  '애'로 끝나는 용언은 하나. (앞의 종성을 넣으면 된다)*/
/* 14: '해'   축약 : trigger character를 'ae'로 바꿈                      */
/* 15: '이+어=여'  : trigger character의 왼쪽의 'y'를 'i'로 바꿈  (체언)  */
/* 15: '이+어=여'  : trigger character의 왼쪽의 'y'를 'i'로 바꿈  (용언)
					 (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */
/* 16: '오+아=와'  : trigger character의 왼쪽의 'w'를 'o'로 바꿈 
					 (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */
/* 16: '우+어=워'  : trigger character의 왼쪽의 'w'를 'u'로 바꿈 
					 (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */
/* 17: '아+아=아'  : trigger character의 왼쪽에 'a'를 삽입 (어간만 조사)
					 (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */
/* 17: '어+어=어'  : trigger character의 왼쪽에 'e'를 삽입 (어간만 조사) 
                     (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */
/* 18: '외+어=왜'  : trigger character를 'ie'로 바꿈 (어간 어미 모두 바뀜) 
                     (다음에 초성 , 어절 끝 , V(ㅆ)이 나와야 된다)        */ 
/* 19: '이 탈락'   : trigger character의 두번째 왼쪽에 'i' 삽입           */ 

